#include <stdio.h>
#include <stdlib.h>
#include <iostream>

using namespace std;


int division( int z, int n, int *rest) // *rest als Zeigerparameter
{
   *rest = z%n; // mit *rest auf den Inhalt zugreifen
   return z/n;
}
int main()
{
   int y,r;
   y = division(5,3,r); // Aufruf mit der Adresse &r
   cout << " Quotient = " << y << " Rest = " << r;
}
