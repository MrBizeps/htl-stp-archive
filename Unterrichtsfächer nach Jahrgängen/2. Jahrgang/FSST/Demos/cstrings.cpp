#include <stdio.h>
#include <iostream>
#include <iomanip>
#include <string.h>                         // C-String Bibliothek

using namespace std;


/////////////////////////////////////////////////////////////////////////////
//   globale C-String Definitionen
/////////////////////////////////////////////////////////////////////////////


/////////////////////////////////////////////////////////////////////////////
//   Hauptprogramm
/////////////////////////////////////////////////////////////////////////////
int main()
 {
   char text[41],trenn[21];                  // C-String als char-Array mit max. 40 Zeichen
   char *pc;

   printf("\n\n Text = ");
   scanf("%s",text);                          // String einlesen
   printf("\n Trennstring = ");
   scanf("%s",trenn);

   pc = strtok(text,trenn);                 // erster Teilstring bis ","
   while (pc)
   {  printf("%s ",pc);
	  pc = strtok(NULL,trenn);              // weitere Teilstrings bis ","
   }

   return 0;
 }
